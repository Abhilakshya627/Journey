#include <iostream>
using namespace std;

int main()
{
    int a,b;
    cout<<"Enter Number in a : ";
    cin>>a;
    cout<<"Enter Number in b : ";
    cin>>b;
    cout<<"Value of a is : ";
    cout<<a;
    cout<<"\nValue of b is : ";
    cout<<b;
    return 0;
}
